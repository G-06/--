SelectBg::SelectBg(void)
{
}

//=============================================================================
// destructor
//=============================================================================
SelectBg::~SelectBg(void)
{
}

//=============================================================================
// initialize
//=============================================================================
bool SelectBg::Initialize(void)
{
	bg_ = new Sprite();
	bg_->Initialize();
	bg_->__size(D3DXVECTOR2((f32)GET_SYSTEM.__window()->__width(),(f32)GET_SYSTEM.__window()->__height()));
	bg_->__position(D3DXVECTOR2(0.0f,0.0f));
	bg_->__texture_id(Texture::TEXTURE_ID_SELECT_BG);
	bg_->__is_flip(true);
	bg_->__left(0.0f);
	bg_->__right(1.0f);
	bg_->SetParameter();
	uv_ = D3DXVECTOR2(0.f,1.0f);
	move_=0.f;
	distmove_=0.f;
	return true;
}

//=============================================================================
// uninitialize
//=============================================================================
void SelectBg::Uninitialize(void)
{
	SafeRelease(bg_);
}

//=============================================================================
// update
//=============================================================================
void SelectBg::Update(void)
{
	bg_->SetParameter();

}

//=============================================================================
// draw
//=============================================================================
void SelectBg::Draw(void)
{
	bg_->Draw();
}


void SelectBg::__set_move_uv(f32 uv_move)
{
	uv_.x+=uv_move;
	uv_.y+=uv_move;

	bg_->__left(uv_.x);
	bg_->__right(uv_.y);
}

