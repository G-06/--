//=============================================================================
// constructor
//=============================================================================
KeyconfigLogo::KeyconfigLogo(void)
{
}

//=============================================================================
// destructor
//=============================================================================
KeyconfigLogo::~KeyconfigLogo(void)
{
}

//=============================================================================
// initialize
//=============================================================================
bool KeyconfigLogo::Initialize(void)
{
	sprite_ = new Sprite();
	sprite_->Initialize();
	sprite_->__size(D3DXVECTOR2((f32)GET_SYSTEM.__window()->__width()/3*2,(f32)GET_SYSTEM.__window()->__height()/5*4));
	sprite_->__position(D3DXVECTOR2((f32)GET_SYSTEM.__window()->__width()/6,(f32)GET_SYSTEM.__window()->__height()/10));
	sprite_->__texture_id(Texture::TEXTURE_ID_CONFIG_BUTTON);
	sprite_->SetParameter();
	return true;
}

//=============================================================================
// uninitialize
//=============================================================================
void KeyconfigLogo::Uninitialize(void)
{
	SafeRelease(sprite_);
}

//=============================================================================
// update
//=============================================================================
void KeyconfigLogo::Update(void)
{
}

//=============================================================================
// draw
//=============================================================================
void KeyconfigLogo::Draw(void)
{
	sprite_->Draw();
}

