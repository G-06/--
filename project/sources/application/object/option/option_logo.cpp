//=============================================================================
// constructor
//=============================================================================
OptionLogo::OptionLogo(void)
{
}

//=============================================================================
// destructor
//=============================================================================
OptionLogo::~OptionLogo(void)
{
}

//=============================================================================
// initialize
//=============================================================================
bool OptionLogo::Initialize(void)
{
	sprite_ = new Sprite();
	sprite_->Initialize();
	sprite_->__size(D3DXVECTOR2(500, 200));
	sprite_->__position(D3DXVECTOR2(D3DXVECTOR2((f32)GET_SYSTEM.__window()->__width()/2,100.0f)));
	sprite_->__point(Sprite::POINT_CENTER);
	sprite_->__texture_id(Texture::TEXTURE_ID_DOT);
	sprite_->SetParameter();
	return true;
}

//=============================================================================
// uninitialize
//=============================================================================
void OptionLogo::Uninitialize(void)
{
	SafeRelease(sprite_);
}

//=============================================================================
// update
//=============================================================================
void OptionLogo::Update(void)
{
}

//=============================================================================
// draw
//=============================================================================
void OptionLogo::Draw(void)
{
	sprite_->Draw();
}

