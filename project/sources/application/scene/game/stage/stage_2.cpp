//=============================================================================
// initialize
//=============================================================================
bool StageTwo::Initialize(void)
{
	NormalStage::Initialize();

	map_ = new Map();

	if(!SafeInitialize(map_))
	{
		return false;
	}

	map_->LoadFromFile("data/map/mario.bin");

	stage_offset_->__stage_size(map_->__size());

	LoadFromFile("data/script/mario.csv");

	return true;
}

