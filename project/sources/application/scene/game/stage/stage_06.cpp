//=============================================================================
// initialize
//=============================================================================
bool StageSix::Initialize(void)
{
	GET_BGM->Play(BGM::BGM_ID_STAGE_06);
	NormalStage::Initialize();

	map_ = new Map();

	if(!SafeInitialize(map_))
	{
		return false;
	}

	map_->LoadFromFile("data/work/arai_stage1.bin");

	stage_offset_->__stage_size(map_->__size());

	LoadFromFile("data/work/arai_stage1.csv");

	return true;
}

