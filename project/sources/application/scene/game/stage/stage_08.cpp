//=============================================================================
// initialize
//=============================================================================
bool StageEight::Initialize(void)
{
	NormalStage::Initialize();

	map_ = new Map();

	if(!SafeInitialize(map_))
	{
		return false;
	}

	map_->LoadFromFile("data/work/tst.bin");

	stage_offset_->__stage_size(map_->__size());

	LoadFromFile("data/work/tst.csv");

	return true;
}

