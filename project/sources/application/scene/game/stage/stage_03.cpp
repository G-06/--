//=============================================================================
// initialize
//=============================================================================
bool StageThree::Initialize(void)
{
	GET_BGM->Play(BGM::BGM_ID_STAGE_03);
	NormalStage::Initialize();

	map_ = new Map();

	if(!SafeInitialize(map_))
	{
		return false;
	}

	//スタンドアップ
	map_->LoadFromFile("data/map/standoUp.bin");

	stage_offset_->__stage_size(map_->__size());

	LoadFromFile("data/script/standoUp.csv");

	return true;
}

