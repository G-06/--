//=============================================================================
// initialize
//=============================================================================
bool StageOne::Initialize(void)
{
	NormalStage::Initialize();

	map_ = new Map();

	if(!SafeInitialize(map_))
	{
		return false;
	}

	map_->LoadFromFile("data/map/stage1.bin");

	stage_offset_->__stage_size(map_->__size());

	LoadFromFile("data/script/stage1.csv");

	return true;
}

