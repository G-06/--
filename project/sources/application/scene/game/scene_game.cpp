#include "scene_game.h"
#include "scene/factory/scene_factory.h"

#include "render/sprite.h"

//=============================================================================
// constructor
//=============================================================================
SceneGame::SceneGame(void) :
Scene(TYPE_GAME)
{
}

//=============================================================================
// destructor
//=============================================================================
SceneGame::~SceneGame(void)
{
}

//=============================================================================
// initialize
//=============================================================================
bool SceneGame::Initialize(void)
{
	sprite_ = new Sprite();
	sprite_->Initialize();
	sprite_->__point(Sprite::POINT_CENTER);
	sprite_->__texture_id(Texture::TEXTURE_ID_TEST);
	sprite_->SetParameter();
	return true;
}

//=============================================================================
// uninitialize
//=============================================================================
void SceneGame::Uninitialize(void)
{
	SafeRelease(sprite_);

	SafeDelete(next_scene_factory_);
}

//=============================================================================
// update
//=============================================================================
void SceneGame::Update(void)
{
	if(next_scene_factory_ == nullptr)
	{
		next_scene_factory_ = new TitleFactory();
	}
}

//=============================================================================
// draw
//=============================================================================
void SceneGame::Draw(void)
{
	sprite_->Draw();
}

//=============================================================================
// create factory
//=============================================================================
SceneFactory* SceneGame::CreateFactory(void)const
{
	return new GameFactory();
}

