//=============================================================================
// constructor
//=============================================================================
TitleBg::TitleBg(void)
{
}

//=============================================================================
// destructor
//=============================================================================
TitleBg::~TitleBg(void)
{
}

//=============================================================================
// initialize
//=============================================================================
bool TitleBg::Initialize(void)
{
	sprite_ = new Sprite();
	sprite_->Initialize();
	sprite_->__size(D3DXVECTOR2((f32)GET_SYSTEM.__window()->__width(),(f32)GET_SYSTEM.__window()->__height()));
	sprite_->__position(D3DXVECTOR2(0.0f,0.0f));
	sprite_->__texture_id(Texture::TEXTURE_ID_TITLE_BG);
	sprite_->SetParameter();
	return true;
}

//=============================================================================
// uninitialize
//=============================================================================
void TitleBg::Uninitialize(void)
{
	SafeRelease(sprite_);
}

//=============================================================================
// update
//=============================================================================
void TitleBg::Update(void)
{
}

//=============================================================================
// draw
//=============================================================================
void TitleBg::Draw(void)
{
	sprite_->Draw();
}

