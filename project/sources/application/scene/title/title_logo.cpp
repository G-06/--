#include "system/system.h"

//*****************************************************************************
// constant definition
//*****************************************************************************
const D3DXVECTOR2 TitleLogo::DEFAULT_POSITION = D3DXVECTOR2(DEFAULT_SCREEN_WIDTH * 0.5f,200.0f);
const D3DXVECTOR2 TitleLogo::DEFAULT_SIZE = D3DXVECTOR2(480.0f,80.0f);

//=============================================================================
// constructor
//=============================================================================
TitleLogo::TitleLogo(void)
	:sprite_(nullptr)
{
}

//=============================================================================
// destructor
//=============================================================================
TitleLogo::~TitleLogo(void)
{
}

//=============================================================================
// initialize
//=============================================================================
bool TitleLogo::Initialize(void)
{
	sprite_ = new Sprite();
	sprite_->Initialize();
	sprite_->__size(DEFAULT_SIZE);
	sprite_->__position(DEFAULT_POSITION);
	sprite_->__point(Sprite::POINT_CENTER);
	sprite_->__texture_id(Texture::TEXTURE_ID_TITLE_LOGO);
	sprite_->SetParameter();
	return true;
}

//=============================================================================
// uninitialize
//=============================================================================
void TitleLogo::Uninitialize(void)
{
	SafeRelease(sprite_);
}

//=============================================================================
// update
//=============================================================================
void TitleLogo::Update(void)
{
}

//=============================================================================
// draw
//=============================================================================
void TitleLogo::Draw(void)
{
	sprite_->Draw();
}

